package Grafica;

public enum Type {
	
	EMPTY("Empty"), STREET("Street"),GENERATOR("Generator"),BASE("Base"),NORMALTOWER("NormalTower"),
	NORMALMONSTER("NormalMonster"),NORMALBULLET("NormalBullet"),FIRETOWER("FireTower");
	
	private String type;
	
	private Type(String type){
		this.type = type;
	}

	@Override
	public String toString(){
		return this.type;
	}
	
	
}
